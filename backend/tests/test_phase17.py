# 6. Empty insights and anomalies do not crash generate_pdf
# ---------------------------------------------------------------------------

def test_generate_pdf_empty_insights_and_anomalies():
    """
    generate_pdf must succeed even with zero insights and zero anomalies.
    This exercises the 'if payload.insights' and 'if payload.anomalies' guards.
    """
    payload = _make_payload(insights=[], anomalies=[])
    pdf_bytes = generate_pdf(payload)

    assert isinstance(pdf_bytes, bytes), "Must return bytes even with empty insights/anomalies"
    assert len(pdf_bytes) > 1_000, "PDF must still be non-trivial with empty lists"
    assert pdf_bytes[:5] == b"%PDF-", "Must still be a valid PDF"

    # Financial summary must still be present
    text = _extract_text(pdf_bytes)
    assert "25,089" in text, "Gross must still appear without insights/anomalies"
    assert "14,339" in text, "Net must still appear without insights/anomalies"
