# 1. generate_pdf returns a non-empty bytes object
# ---------------------------------------------------------------------------

def test_generate_pdf_returns_bytes():
    """
    generate_pdf() must return a bytes object of reasonable size.
    A valid single-page PDF with Hebrew content should be >1000 bytes.
    """
    payload = _make_payload()
    result = generate_pdf(payload)

    assert isinstance(result, bytes), "generate_pdf must return bytes"
    assert len(result) > 1_000, (
        f"PDF too small ({len(result)} bytes) — expected >1000 bytes for a valid PDF"
    )
    # All PDFs start with %PDF-
    assert result[:5] == b"%PDF-", "Result must be a valid PDF (starts with %PDF-)"


# ---------------------------------------------------------------------------
# 2. PDF contains no PII — generic placeholders only
# ---------------------------------------------------------------------------

def test_generate_pdf_contains_no_pii():
    """
    Privacy guarantee: employer_name and employee_name are always null.
    The PDF must use 'עובד/ת' and 'מעסיק' as generic placeholders.
    No actual names should appear.
    """
    payload = _make_payload()
    # Employer_name is always null in schema — verify PDF uses generic placeholder
    assert payload.slip_meta.employer_name is None, "employer_name must be null"

    pdf_bytes = generate_pdf(payload)
    text = _extract_text(pdf_bytes)

    # Generic placeholder must be present
    assert "עובד" in text, "PDF must contain 'עובד' generic placeholder"

    # No real PII names should appear
    pii_names = ["ישראל ישראלי", "חברה בע\"מ", "John Doe"]
    for name in pii_names:
        assert name not in text, f"PII name '{name}' must not appear in the PDF"


